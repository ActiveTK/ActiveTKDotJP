  function whois($domain)
  {
    $tld = getTLD($domain);
    global $whois_lists;
    if (!isset($whois_lists[$tld]))
      return 'エラーが発生しました。'."\n".'ドメインのTLDを所有しているwhoisサーバーが見つかりませんでした。';
    $errno = 0;
    $errstr = '';
    $body = '';
    $fp = fsockopen($whois_lists[$tld][0], 43, $errno, $errstr);
    if (!$fp) {
      echo "エラーが発生しました。\n$errstr ($errno)\n";
    } else {
      fputs($fp, $domain."\r\n" );
      while (!feof($fp)) {
        $body .= fgets($fp);
      }
      fclose($fp);
    }
    return $body;
  }
  function whoisiana($domain)
  {
    $errno = 0;
    $errstr = '';
    $body = '';
    $fp = fsockopen("whois.iana.org", 43, $errno, $errstr);
    if (!$fp) {
      echo "エラーが発生しました。\n$errstr ($errno)\n";
    } else {
      fputs($fp, $domain."\r\n" );
      while (!feof($fp)) {
        $body .= fgets($fp);
      }
      fclose($fp);
    }
    return $body;
  }
  function getTLD($domain)
  {
    if (($pos = strrpos($domain, '.')) !== false) {
      return strtolower(substr($domain, $pos+1));
    } else {
      return '';
    }
  }
  function lookup($domain)
  {
    $res = dns_get_record($domain, DNS_A);
    if ($res === false)
      return "エラーが発生しました。: ドメインが存在しません。";
    return $res;
  }
